/* Flags must be powers of 2 */
/* Slot has data */
#define ACTIVE 1
/* Slot had its value deleted */
#define DELETED 2
/* Slot is unused */
#define EMPTY 0

#define MAX(a,b) ((a) > (b) ? (a) : (b))

